# ⭐ 판매·출고 관점 구분 규칙 (절대 준수)

판매·출고 데이터는 완전히 분리된 2개 축으로 존재합니다. 각각 별개로 다루고, 절대 합산하지 않습니다.

## 축 1 — ERP 축 (지엘 3법인 거래)
- 유일 소스: `orders` 테이블
- 단위: `item_master`의 144개 내부 품목
- 포함 법인: gl(지엘), glpharm(지엘팜), hnb
- 관련 뷰: `v_unified_orders_dashboard`, `v_orders_summary`

## 축 2 — 쿠팡 축 (쿠팡 채널)
- 소스: `daily_performance`(B2C 판매), `coupang_delivery_detail`(쿠팡 납품),
  `inventory_operation`(쿠팡 센터 재고), `bi_box_daily`(바이박스) 등
- 단위: `sku_master.sku_id` 쿠팡 SKU (59개)

## 두 축의 연결 (합산이 아님)
`item_coupang_mapping`은 "내부 item_id ↔ 쿠팡 sku_id"를 1:N으로 연결합니다.
용도는 **보충(replenishment) 의사결정** — 쿠팡 재고 부족 시 자사 재고로 충당 가능한지 판단.
두 축의 수치를 더하거나 평균 내지 않습니다.

## 기본 동작
사용자가 "매출", "판매량"만 말하고 채널이 모호하면:
- 품목을 SKU(HK001 등)로 지정 → 쿠팡 축 답변
- 품목을 품목명·item_id로 지정 → ERP 축 답변
- 법인(지엘, 지엘팜, HNB)을 언급 → ERP 축 답변
- 쿠팡·로켓배송·바이박스 언급 → 쿠팡 축 답변
- 어느 쪽도 명확하지 않으면 쿠팡 축을 기본으로 하되 답변 첫 줄에 "(쿠팡 B2C 기준)"을 명시하고, 맨 아래에 "ERP 기준으로 다시 보시려면 말씀해 주세요" 안내
- 두 축이 모두 필요한 질문(예: 쿠팡 부족분을 자사에서 충당 가능?)은 두 축 수치를 **나란히 표시**

# ⭐ 재고 관점 구분 규칙 (절대 준수)

재고도 물리적으로 별개의 두 창고에 분리되어 있습니다.

- **지엘 본사 창고 재고** (`stock_movement.running_stock` 기준)
  자사 창고 실재고. 144개 내부 item_master 단위.
  orders 승인 시 자동 변동. 쿠팡 센터로 보내도 감소(외부 출고).

- **쿠팡 센터 재고** (`inventory_operation.current_stock` 기준)
  쿠팡 풀필먼트 센터(이천·덕평 등)에 있는 지엘 제품의 재고. 쿠팡 SKU 단위.
  쿠팡 B2C 판매로 감소. 지엘이 밀크런·납품으로 보충.

질문에서 "재고"가 어느 것인지 모호하면 축을 명시하거나 되묻습니다.
- SKU(HK001, HK005 등)로 물으면 → 쿠팡 센터 재고
- 품목명·item_id로 물으면 → 지엘 본사 재고
- "재고 부족", "결품 위험" 같은 보충 판단 질문이면 두 재고를 나란히 표시
  (반드시 `item_coupang_mapping.bundle_ratio`로 단위 환산)

# ⭐ 승인 상태 처리 규칙 (orders 전용, 절대 준수)

`orders.status`는 세 상태로 분리됩니다: `approved`(승인), `pending`(승인 대기), `rejected`(반려).

## 매출·판매 집계 기본값
"이번 달 매출", "지엘팜 판매 얼마" 같은 **집계 질문은 기본적으로 `status='approved'` + `is_internal=false`**만 사용합니다.

- `pending`은 집계에서 제외 — 아직 확정 거래가 아님
- `rejected`는 집계에서 제외 — 취소된 거래
- `is_internal=true`는 자사 3법인 간 내부거래라 외부 매출에서 제외

사용자가 명시적으로 "승인 대기 포함", "반려 포함"이라고 요청해도 **합쳐서 합계를 보여주지 않습니다**. 대신 상태별로 분리해 각자의 합계를 나란히 표시합니다.

예시:
- "이번 달 매출" → status='approved' 합계만 (기본)
- "이번 달 전체 매출(대기 포함)" → status별로 분리 표시:
  - 승인 매출: X원
  - 승인 대기 매출: Y원
  - 반려 매출: Z원

## 승인 관리 유스케이스
- "승인 대기 몇 건?" → status='pending' count
- "반려된 발주 이유?" → status='rejected' + rejected_reason
  이 질문들은 집계가 아니라 **운영 상태 조회**이므로 각 status를 단독으로 조회.

